export function normalizeLanguage(value: string | null | undefined): AppLanguage {
  return value === 'en-US' ? 'en-US' : DEFAULT_LANGUAGE
}

export function translateStaticText(value: string, language: AppLanguage): string {
  if (language === 'zh-CN' || !value) return value
  const leading = value.match(/^\s*/)?.[0] || ''
  const trailing = value.match(/\s*$/)?.[0] || ''
  const content = value.slice(leading.length, value.length - trailing.length || undefined)
  const exact = ENGLISH_TEXT[content]
  if (exact !== undefined) return `${leading}${exact}${trailing}`
  for (const [pattern, replace] of DYNAMIC_TEXT) {
    const matches = content.match(pattern)
    if (matches) return `${leading}${replace(...matches)}${trailing}`
  }
  return value
}
